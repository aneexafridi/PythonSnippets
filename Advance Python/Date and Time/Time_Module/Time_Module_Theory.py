# --------------------------Time Modules
# >> 1
# time() Function - This fucntion is return the time in seconds since the
# epoch as a floating point number. The specific date of the epoch and the
# handling of leap seconds is platform dependent.
# >> 2
# ctime() Function - This function is used to get current date and time.
# When we pass eoch time in seconds to the function, it returns corresponding
# date and time in string format. When we do not pass epoch time,it returns
# current date and time in string format.
# >> 3
# localtime() Fucntion - This function is used to convert seconds into date and
# time. It returns an object struct_time Which can be used to access the
# attributes either using an index or using a name.
