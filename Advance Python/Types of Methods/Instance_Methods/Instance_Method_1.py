# --------------Instannce Method
# Instannce methods are the methods which act upon the 
# Instannce variables of the class.
# Instannce methods need to know the memory address of
# the Instannce/Object which is provided through
# "self" variable by default as first parameter for the
# Instannce methods.
# Syntax:---
# Instannce method without parameter/formal arguments
# 		def method_Name(self):
# 			function body
# Instannce method with parameter/formal arguments
# 		def Method_Name(self,p1,p2):
# 			function body