# ------------Clear Method
# clear() this method is used to remove all the elements
# from the exists Dictionary
#   Syntax:===
			#Dictionary_Name.clear()
Dict = {1:"one",2:"two",3:"three"}
print(Dict)
print("The length of Dictionary: ",len(Dict))
Dict.clear()
print("After Clear method used")
print(Dict)
print("The length of Dictionary: ",len(Dict))
