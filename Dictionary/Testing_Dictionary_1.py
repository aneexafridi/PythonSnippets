# ------------Testing Key
# We can check whether a key is already exists in 
# the dictionary or not,for this purpose we use
# membership operator.
Dict = {1:"one",2:"two",3:"three"}
print(Dict)

# using membership operator in this case

print(1 in Dict) # if key exist in Dict then return True
# if key doesn't exist in the Dict then return True
print(5 not in Dict)

