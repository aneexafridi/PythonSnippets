# sort() Method
# this method is used to sort the elements of the list into ascending order.
# Syntax:- List_Name.sort()

# -----------------Note------------
# to use sort() method  must be list in the same data type

List = ['Shahab','Fawad','Ali','Hassan','Bilal']
print(List)
List.sort()
print(List)

Numbers = [2,4,1,5,3]
print(Numbers)
Numbers.sort()
print(Numbers)