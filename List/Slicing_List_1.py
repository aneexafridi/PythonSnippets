# Slicing on List
# Slicing on list can be used to retrieve a piece of the list
# that contains a group of elements.Slicing is useful to
# retrieve a range of elements.
# Syntax:-- New_List_Name = List_Name[start:end:step]

List = [1,2,3,4,5,6]
print(List)
Elements = List[1::2] # start=1 ,end=deflaut=all remain,step=2
print(Elements)