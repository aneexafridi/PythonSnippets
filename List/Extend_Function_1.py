# extend() Method
# This method is used to append another List or
# iterable object at the end of the list.
# Syntax:- List_Name.extend(another_list)

List_1 = [1,2,3,4]
List_2 = [5,6,7,8]
print("List_1: ",List_1)
print("List_2: ",List_2)
List_1.extend(List_2) # extend mehtod use
print("Afer extend mehtod use")
print("List_1: ",List_1)
print("List_2: ",List_2)