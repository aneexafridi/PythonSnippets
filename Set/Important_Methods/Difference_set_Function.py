 # --------------difference Function
# difference() Return a new set with elements
# in the set that are not in the others.

# Syntax:-- Set_1.difference(Set_2)
			

Set_1 = {"A","B","F","G","Z"}
Set_2 = {"B","D","H","Z","A"}
print(Set_1)
print(Set_2)
print("After Set_1 difference Set_2")
print(Set_1.difference(Set_2))