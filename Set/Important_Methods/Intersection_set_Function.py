# --------------InterSection Function
# intersection() Return a new set with elements common
#to the set and all others.

# Syntax:-- Set_1.intersection(Set_2)
			

Set_1 = {"A","B","F","G","Z"}
Set_2 = {"B","D","H","Z","A"}
print(Set_1)
print(Set_2)
print("After Set_1 InterSection Set_2")
print(Set_1.intersection(Set_2))