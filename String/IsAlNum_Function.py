# isalnum() Function
# this method returns True if the String has at least
# one character and all characters in the string are
# alphanumeric (A-Z and a-z and 0-9)
# else returns False
# Syntax:- string.isalnum()
AlNum = "Equal123"
print(AlNum)
print(AlNum.isalnum())
print("Equal".isalnum())
print("123".isalnum())
print("Equal)Equal".isalnum())