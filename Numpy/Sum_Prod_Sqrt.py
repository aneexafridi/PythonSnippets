from numpy import *
A = array([1,2,3,4])
B = array([[1,2,3,4],[5,6,7,8]])
print("A: ",A) # 1D array
print("B: ",B) # 2D array
# sum() function

# Returns sum(arr) of the all elements in the array arr
print("\nSum of A:",sum(A))
print("Sum of B:",sum(B))
# prod() Function
# Returns product(arr) of the all elements in the array arr
print("\nproduct of A:",prod(A))
print("porduct of B:",prod(B))
# sqrt() function
# Returns square root value of each element in the array arr
print("\nSquare root of A:",sqrt(A))
print("Square root of B:",sqrt(B))
